#include "pch.h"
#include "COperations.h"

float COperations::add(float num1, float num2)
{
	return num1 + num2;
}

float COperations::subtract(float num1, float num2)
{
	return num1 - num2;
}

float COperations::multiply(float num1, float num2)
{
	return num1 * num2;
}

float COperations::divide(float num1, float num2)
{
	return num1 / num2;
}